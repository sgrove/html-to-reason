const getAttributeCanonicalName = (name) => {
  return REASON_REACT_DOM_ATTRIBUTE_CANONICAL_NAMES[name.toUpperCase()] || name;
};

const escapeQuotes = (string) => {
  if (typeof string === "string") {
    return string.replace(/\\"/gm, '\\\\"').replace(/([^\\])"/g, '$1\\"');
  }

  return string;
};

const DASH = /[-|_|:]([a-z])/g;
const MS = /^Ms/g;

function capitalize(match) {
  return match[1].toUpperCase();
}

function lowerCaseFirst(s) {
  return s.charAt(0).toLowerCase() + s.slice(1);
}

function camelCase(property) {
  const mid = property.replace(DASH, capitalize).replace(MS, "ms");
  const final = lowerCaseFirst(mid);

  return final;
}

const attributeCoercions = {
  string: (name, string) => `${name}="${escapeQuotes(string)}"`,
  bool: (name, value) => {
    let coerced = value;
    if (typeof value == "string") {
      coerced = camelCase(coerced);
      coerced = value.toUpperCase() === "TRUE" || value === "";
    }
    return `${name}=${coerced}`;
  },
  int: (name, value) => {
    try {
      return `${name}=(${parseInt(value, 10)})`;
    } catch (e) {
      console.warn(
        `Failed to parse int for attribute ${name}, failure: ${e}. Returning original value.`
      );
      return value;
    }
  },
  float: (name, value) => {
    try {
      return `${name}=(${parseFloat(value, 10)})`;
    } catch (e) {
      console.warn(
        `Failed to parse float for attribute ${name}, failure: ${e}. Returning original value version.`
      );
      return value;
    }
  },
  function: (name, value) => {
    // If the html has e.g. an onClick handler set in the html, we expect it to
    // be a variable name, so leave it unquoted. If it's blank, then assign it the
    // same name as the attribute
    //
    // ... *unless* it contains spaces, in which case it might be inline-JS (hacker news...)
    // so quote it, and don't camelCase the value

    let coerced;

    if ((value || "").trim().includes(" ")) {
      coerced = `"${escapeQuotes(value)}"`;
    } else {
      coerced = camelCase(value) || camelCase(name);
    }

    return `${name}=${coerced}`;
  },
  "ReactDOMStyle.t": (name, value) => {
    if (value === "") {
      console.warn("Found empty style for: ", name, value);
      return `${name}={ReactDOMStyle.make()}`;
    }
    return `${name}={${value}}`;
  },
  identityCoercer: (name, value) => `${name}="${escapeQuotes(value)}"`,
};

const getCoercerForAttribute = (name) => {
  const coercerType = REASON_REACT_DOM_ATTRIBUTE_COERCIONS[name];
  return (
    attributeCoercions[coercerType] || attributeCoercions["identityCoercer"]
  );
};

var SINGLE_TAGS = [
  "area",
  "base",
  "br",
  "col",
  "command",
  "embed",
  "hr",
  "img",
  "input",
  "keygen",
  "link",
  "menuitem",
  "meta",
  "param",
  "source",
  "track",
  "wbr",
  // Custom (PostHTML)
  "import",
  "include",
  "extend",
];

let makeWrapperNode = (children) => {
  return {
    tag: "div",
    children: children,
  };
};

var ATTRIBUTE_QUOTES_REQUIRED = /[\t\n\f\r " '`=<>]/;

/** Render PostHTML Tree to HTML
 *
 * @param  {Array|Object} tree PostHTML Tree @param  {Object} options Options
 *
 * @return {String} HTML
 */
function render(tree, options) {
  /** Options
   *
   * @type {Object}
   *
   * @prop {Array<String|RegExp>} singleTags  Custom single tags (selfClosing)
   * @prop {String} closingSingleTag Closing format for single tag @prop
   * @prop {Boolean} quoteAllAttributes If all attributes should be quoted.
   * Otherwise attributes will be unquoted when allowed.
   *
   * Formats:
   *
   * ``` tag: `<br></br>` ```, slash: `<br />` ```, ```default: `<br>` ```
   */
  options = options || {};

  var singleTags = options.singleTags
    ? SINGLE_TAGS.concat(options.singleTags)
    : SINGLE_TAGS;
  var singleRegExp = singleTags.filter(function (tag) {
    return tag instanceof RegExp;
  });

  var closingSingleTag = options.closingSingleTag;
  var quoteAllAttributes = options.quoteAllAttributes;
  if (typeof quoteAllAttributes === "undefined") {
    quoteAllAttributes = true;
  }

  return html(tree);

  /** @private */
  function isSingleTag(tag) {
    if (singleRegExp.length !== 0) {
      for (var i = 0; i < singleRegExp.length; i++) {
        return singleRegExp[i].test(tag);
      }
    }

    if (singleTags.indexOf(tag) === -1) {
      return false;
    }

    return true;
  }

  /** @private */
  function attrs(obj) {
    var attr = " ";

    for (var key in obj) {
      const rawValue = obj[key];
      const value =
        rawValue.toUpperCase() === "FALSE"
          ? false
          : rawValue.toUpperCase() === "TRUE"
          ? true
          : rawValue;

      const coercer = getCoercerForAttribute(key);
      attr += coercer(key, value) + " ";
    }

    return attr;
  }

  /** @private */
  function traverse(tree, cb) {
    if (tree !== undefined) {
      for (var i = 0, length = tree.length; i < length; i++) {
        traverse(cb(tree[i]), cb);
      }
    }
  }

  /**
   * HTML Stringifier
   *
   * @param  {Array|Object} tree PostHTML Tree
   *
   * @return {String} result HTML
   */
  function htmlHelper(level, tree) {
    var result = "";

    if (!Array.isArray(tree)) {
      tree = [tree];
    }

    traverse(tree, function (node) {
      // undefined, null, '', [], NaN
      if (
        node === undefined ||
        node === null ||
        node === false ||
        node.length === 0 ||
        Number.isNaN(node)
      ) {
        return;
      }

      // Rudimentary indentation before refmt can help
      const whitespace = new Array(level).fill("  ").join("");

      // treat as new root tree if node is an array
      if (Array.isArray(node)) {
        result += htmlHelper(level + 1, node);

        return;
      }

      if (typeof node === "string" || typeof node === "number") {
        result += node;

        return;
      }

      // skip node
      if (node.tag === false) {
        result += htmlHelper(level + 1, node.content);

        return;
      }

      var tag = node.tag || "div";

      result += " <" + tag;

      if (node.attrs) {
        result += attrs(node.attrs);
      }

      if (isSingleTag(tag)) {
        switch (closingSingleTag) {
          case "tag":
            result += "> </" + tag + ">\n" + whitespace;

            break;
          case "slash":
            result += " />\n" + whitespace;

            break;
          default:
            result += ">\n" + whitespace;
        }

        result += htmlHelper(level + 1, node.content);
      } else {
        result +=
          "> " +
          htmlHelper(level + 1, node.content) +
          " </" +
          tag +
          ">\n" +
          whitespace;
      }
    });

    return result;
  }

  function html(tree) {
    if (tree && tree[0] && tree[0]["tag"] === "undefined") {
      tree = makeWrapperNode(tree);
    }

    return htmlHelper(0, tree);
  }
}

